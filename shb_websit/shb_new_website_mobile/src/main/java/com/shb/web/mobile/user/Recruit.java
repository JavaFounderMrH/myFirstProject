package com.shb.web.mobile.user;

import java.io.Serializable;

@SuppressWarnings("serial")
public class Recruit implements Serializable{
	
    private Long id; // 房东招商管理ID

    private String name; // 联系人姓名

    private String cityName; // 所在城市名称

    private String phone; // 联系方式

    private String createTime; // 提交时间

    private Long stats; // 状态

    private String visitTime; // 回访时间

    private String visitName; // 回访人姓名

    private Long type; // 类型(1:房东招商管理   2:合伙人招商管理)

    private String visitSyn; // 回访内容

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCityName() {
		return cityName;
	}

	public void setCityName(String cityName) {
		this.cityName = cityName;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getCreateTime() {
		return createTime;
	}

	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}

	public Long getStats() {
		return stats;
	}

	public void setStats(Long stats) {
		this.stats = stats;
	}

	public String getVisitTime() {
		return visitTime;
	}

	public void setVisitTime(String visitTime) {
		this.visitTime = visitTime;
	}

	public String getVisitName() {
		return visitName;
	}

	public void setVisitName(String visitName) {
		this.visitName = visitName;
	}

	public Long getType() {
		return type;
	}

	public void setType(Long type) {
		this.type = type;
	}

	public String getVisitSyn() {
		return visitSyn;
	}

	public void setVisitSyn(String visitSyn) {
		this.visitSyn = visitSyn;
	}

}