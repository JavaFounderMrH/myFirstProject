package com.shb.web.mobile.room;

import java.io.Serializable;

@SuppressWarnings("serial")
public class ReservationRoom implements Serializable{
	
	private  Long sriId; // 房間ID
	
	private Integer whetherOvernight; // 是否隔夜 0 ：不隔夜 1：隔夜
	
	private Double price; // 价格
	
	private String picSrc; // 房间图片
	
	private Integer checkNumber; // 入住人數
	
	private String roomTitle; // 房间标题
	
	private Integer bedroomNumber; // 室數
	
	private Integer livingRoomNumber; // 厅数
	
	private Integer toiletNumber; // 卫数
	
	private Integer bedNumber; // 床位数
	
	private Integer leaseType; // 出租类型
	
	private Integer whertherRefund; // 是否支持退款
	
	private Integer roomUpDay;
	
	public Integer getRoomUpDay() {
		return roomUpDay;
	}

	public void setRoomUpDay(Integer roomUpDay) {
		this.roomUpDay = roomUpDay;
	}

	public Integer getLeaseType() {
		return leaseType;
	}

	public void setLeaseType(Integer leaseType) {
		this.leaseType = leaseType;
	}

	public Integer getWhertherRefund() {
		return whertherRefund;
	}

	public void setWhertherRefund(Integer whertherRefund) {
		this.whertherRefund = whertherRefund;
	}

	public Long getSriId() {
		return sriId;
	}

	public void setSriId(Long sriId) {
		this.sriId = sriId;
	}

	public Integer getWhetherOvernight() {
		return whetherOvernight;
	}

	public void setWhetherOvernight(Integer whetherOvernight) {
		this.whetherOvernight = whetherOvernight;
	}

	public Double getPrice() {
		return price;
	}

	public void setPrice(Double price) {
		this.price = price;
	}

	public String getPicSrc() {
		return picSrc;
	}

	public void setPicSrc(String picSrc) {
		this.picSrc = picSrc;
	}

	public Integer getCheckNumber() {
		return checkNumber;
	}

	public void setCheckNumber(Integer checkNumber) {
		this.checkNumber = checkNumber;
	}

	public String getRoomTitle() {
		return roomTitle;
	}

	public void setRoomTitle(String roomTitle) {
		this.roomTitle = roomTitle;
	}

	public Integer getBedroomNumber() {
		return bedroomNumber;
	}

	public void setBedroomNumber(Integer bedroomNumber) {
		this.bedroomNumber = bedroomNumber;
	}

	public Integer getLivingRoomNumber() {
		return livingRoomNumber;
	}

	public void setLivingRoomNumber(Integer livingRoomNumber) {
		this.livingRoomNumber = livingRoomNumber;
	}

	public Integer getToiletNumber() {
		return toiletNumber;
	}

	public void setToiletNumber(Integer toiletNumber) {
		this.toiletNumber = toiletNumber;
	}

	public Integer getBedNumber() {
		return bedNumber;
	}

	public void setBedNumber(Integer bedNumber) {
		this.bedNumber = bedNumber;
	}

	public String getCheckTime() {
		return checkTime;
	}

	public void setCheckTime(String checkTime) {
		this.checkTime = checkTime;
	}

	public String getCheckOutTime() {
		return checkOutTime;
	}

	public void setCheckOutTime(String checkOutTime) {
		this.checkOutTime = checkOutTime;
	}

	private String checkTime;  // 入住时间段
	
	private String checkOutTime; // 离店时间段

}
