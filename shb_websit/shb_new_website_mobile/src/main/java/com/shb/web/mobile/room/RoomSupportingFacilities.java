package com.shb.web.mobile.room;

import java.io.Serializable;

@SuppressWarnings("serial")
public class RoomSupportingFacilities implements Serializable{
	
    private Integer srsfId; // 配套设施主键ID

    private Integer type; // 配套设施 类型

    private String name; // 名称
    
    private String src;

    public String getSrc() {
		return src;
	}

	public void setSrc(String src) {
		this.src = src;
	}

	public Integer getSrsfId() {
        return srsfId;
    }

    public void setSrsfId(Integer srsfId) {
        this.srsfId = srsfId;
    }

    public Integer getType() {
        return type;
    }

    public void setType(Integer type) {
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name == null ? null : name.trim();
    }

}